package GDMASTERB1.config;

public class HttpConfException extends RuntimeException {
    public HttpConfException() {
    }

    public HttpConfException(String message) {
        super(message);
    }

    public HttpConfException(String message, Throwable cause) {
        super(message, cause);
    }

    public HttpConfException(Throwable cause) {
        super(cause);
    }


}
